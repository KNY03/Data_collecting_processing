select * from 주문;

-- 1. ‘서울특별시’에 사는 고객에 대해 고객번호, 담당자명, 도시, 마일리지를 조회하시오. 이때 마일리지가 많은 고객부터 순서대로 보입니다.
SELECT 고객번호, 담당자명, 도시, 마일리지 FROM 고객 where 도시 in ("서울특별시") order by 마일리지 desc;

-- 2. 마일리지가 많은 고객부터 상위 3명의 고객에 대한 모든 정보를 조회하시오.
SELECT * FROM 고객 order by 마일리지 desc limit 3;

-- 3. 고객 테이블의 도시 컬럼에 들어있는 값 중 중복되는 도시 데이터를 한 번씩만 보이시오.
select distinct 도시 from 고객;

-- 4. 담당자직위가 ‘영업 과장’이거나 ‘마케팅 과장’인 고객에 대하여 고객번호, 담당자명, 담당자직위를 보이시오.
select 고객번호, 담당자명, 담당자직위 From 고객  where 담당자직위 = "영업 과장" OR 담당자직위 = "마케팅 과장";

-- 5. 마일리지가 100,000점 이상 200,000점 이하인 고객에 대해 담당자명, 마일리지를 보이시오.
select 담당자명, 마일리지 From 고객 where 마일리지 >= 100000 AND 마일리지 <= 200000;

-- 6. 고객 테이블에서 고객번호, 도시, 지역의 개수를 조회하시오.
SELECT  count(distinct 고객번호), count(distinct 도시), count(distinct 지역) FROM 고객;

-- 7. 고객 테이블에서 ‘서울특별시’ 고객에 대해 마일리지합, 평균마일리지, 최소마일리지, 최대마일리지를 조회하시오.
SELECT SUM(마일리지), AVG(마일리지), MIN(마일리지), MAX(마일리지) FROM 고객 WHERE 도시 in ("서울특별시");

-- 8. 고객 테이블에서 도시별 고객의 수와 해당 도시 고객들의 평균마일리지를 조회하시오.
SELECT 도시,COUNT(고객번호) AS "고객 수", AVG(마일리지) AS "평균 마일리지"
	FROM 고객
    GROUP BY 도시;
    
-- 9. 담당자직위별로 묶고, 같은 담당자직위에 대해서는 도시별로 묶어서 집계한 결과(고객수와 평균마일리지)를 보이시오. (이때 담당자직위 순, 도시 순으로 정렬하기)
SELECT 담당자직위, 도시, COUNT(고객번호) AS "고객 수", AVG(마일리지) AS "평균 마일리지"
	FROM 고객
    GROUP BY 담당자직위, 도시
    ORDER BY 담당자직위, 도시;

-- 10. 고객 테이블에서 도시별로 그룹을 묶어서 고객수와 평균마일리지를 구하고, 이 중에서 고객수가 10명 이상인 레코드만 걸러내시오.
SELECT count(고객번호) AS "고객수", AVG(마일리지) AS "평균 마일리지"
	FROM 고객
    GROUP BY 도시
    HAVING count(도시) >= 10;

